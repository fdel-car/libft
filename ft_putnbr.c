#include "libft.h"

void	ft_putnbr(long int n)
{
	if (n < 0)
	{
		ft_putchar('-');
		n = -n;
	}
	if (n > 9)
	{
		ft_putnbr(n / 10);
		ft_putnbr(n % 10);
		return ;
	}
	else
	{
		ft_putchar(n + 48);
	}
}
